package chengyu.bean;

public class Idoms {
	public int id;
	public String name;
	public int sort;
	public String video;
	public String img;
	public String descri_ch;
	public String descri_py;
	public String story_ch;
	public String story_py;
	
	public int getid() {
		return id;
	}
	
	public void setid(int id) {
		this.id = id;
	}
	
	public String getname() {
		return name;
	}
	public void setname(String name) {
		this.name = name;
	}
	
	public int getSort() {
		return sort;
	}
	
	public void setSort(int sort) {
		this.sort = sort;
	}
	
	public String getVideo() {
		return video;
	}
	
	public void setVideo(String video ) {
		this.video = video;
	}
	
	public String getImg() {
		return img;
	}
	
	public void setImg(String img ) {
		this.img = img;
	}
	
	public String getDescri_ch() {
		return descri_ch;
	}
	
	public void setDescri_ch(String descri_ch ) {
		this.descri_ch = descri_ch;
	}
	
	public String getDescri_py() {
		return descri_py;
	}
	
	public void setDescri_py(String descri_py) {
		this.descri_py = descri_py;
	}
	
	public String getStory_ch() {
		return story_ch;
	}
	
	public void setStory_ch(String story_ch) {
		this.story_ch = story_ch;
	}
	
	public String getStory_py() {
		return story_py;
	}
	
	public void setStory_py(String story_py) {
		this.story_py = story_py;
	}
}
